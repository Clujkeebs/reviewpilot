/* ── Score ───────────────────────────────────────────────────────────────── */
function renderScore(score) {
  const overall     = clamp(score.overall, 0, 100);
  const breakdown   = score;
  const circumference = 339.3;

  const scoreColor = overall >= 75 ? '#059669' : overall >= 50 ? '#f59e0b' : '#dc2626';

  document.getElementById('card-score').innerHTML = `
    <div class="card-title"><span class="card-title-icon">📊</span> GBP Health Score</div>
    <div class="score-ring-wrap">
      <svg viewBox="0 0 120 120">
        <circle class="ring-bg" cx="60" cy="60" r="54" />
        <circle class="ring-fg" cx="60" cy="60" r="54" id="ring-fg"
                style="stroke:${scoreColor}" />
      </svg>
      <div class="score-center">
        <span class="score-num" id="score-num">0</span>
        <span class="score-denom">/ 100</span>
      </div>
    </div>
    <p class="score-verdict">${esc(score.verdict)}</p>
    <div class="score-breakdown">
      ${makeBar('Description', breakdown.description)}
      ${makeBar('Categories',  breakdown.categories)}
      ${makeBar('Photos',      breakdown.photos)}
      ${makeBar('Posts',       breakdown.posts)}
      ${makeBar('Q&A',         breakdown.qanda)}
    </div>
  `;

  // Animate ring
  requestAnimationFrame(() => {
    const fg = document.getElementById('ring-fg');
    if (fg) fg.style.strokeDashoffset = circumference * (1 - overall / 100);
    animateNum('score-num', 0, overall, 1400);
    document.querySelectorAll('.sb-fill').forEach(el => {
      el.style.width = el.dataset.val + '%';
    });
  });
}

function makeBar(label, val) {
  const v     = clamp(Number(val) || 0, 0, 100);
  const color = v >= 75 ? '#059669' : v >= 50 ? '#f59e0b' : '#dc2626';
  return `
    <div class="sb-row">
      <span class="sb-label">${esc(label)}</span>
      <div class="sb-track"><div class="sb-fill" data-val="${v}" style="background:${color}"></div></div>
      <span class="sb-val">${v}</span>
    </div>`;
}

