/* ── Loading steps animation ─────────────────────────────────────────────── */
let stepTimer;
function startLoadingSteps() {
  const steps = document.querySelectorAll('.lstep');
  let current = 0;
  steps.forEach(s => s.classList.remove('active', 'done'));
  steps[0].classList.add('active');

  stepTimer = setInterval(() => {
    if (current < steps.length - 1) {
      steps[current].classList.remove('active');
      steps[current].classList.add('done');
      current++;
      steps[current].classList.add('active');
    }
  }, 3000);
}
function stopLoadingSteps() {
  clearInterval(stepTimer);
  document.querySelectorAll('.lstep').forEach(s => { s.classList.remove('active'); s.classList.add('done'); });
}

/* ── Visibility helpers ──────────────────────────────────────────────────── */
function showLoading()  { formCard.style.display = 'none'; oLoading.classList.remove('hidden'); }
function hideLoading()  { stopLoadingSteps(); oLoading.classList.add('hidden'); }
function showResults()  { oResults.classList.remove('hidden'); }
function showForm()     { formCard.style.display = ''; }
function setBusy(busy)  {
  analyzeBtn.disabled = busy;
  analyzeBtnTxt.textContent = busy ? 'Analyzing…' : 'Analyze My Google Profile →';
  analyzeSpinner.classList.toggle('hidden', !busy);
}
function showError(msg) {
  errorBox.textContent = msg;
  errorBox.classList.remove('hidden');
  errorBox.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
}

